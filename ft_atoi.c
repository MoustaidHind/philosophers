#include "philo.h"

long	ft_atoi(const char *str)
{
	int		i;
	long	result;
	int		sign;
	int		check;

	i = 0;
	result = 0;
	sign = 1;
	check = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i++] - 48);
		if (result > INT_MAX || result < INT_MIN)
			return (-1);
		check++;
	}
	return (result * sign);
}
